public class Place {
    public int placeType ;
    public double x ;
    public double y ;
    public int lineNumber;

    public Place() {}

    public Place(int placeType, double x, double y, int lineNumber) {
        this.placeType = placeType;
        this.x = x;
        this.y = y;
        this.lineNumber = lineNumber;
    }

}
